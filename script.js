const btnProject = document.querySelector(".btn-project");
const btnContact = document.querySelector(".btn-contact");

const time = 10000;
let active = 0;

console.log(popup);

const changeSlide = () => {
  if (active === slideList.length) {
  }
  image.src = slideList[active].img;
  h1.textContent = slideList[active].text;
  popup = slideList[active].popup;
  active++;
  console.log(popup);
};
// let indexInterval = setInterval(changeSlide, time);

const keyChangeSlide = (e) => {
  if (e.keyCode === 37 || e.keyCode === 39) {
    // clearInterval(indexInterval);

    e.keyCode == 37 ? active-- : active++;

    if (active == slideList.length) {
    } else if (active < 0) {
      active = slideList.length - 1;
    }
    image.src = slideList[active].img;
    h1.textContent = slideList[active].text;
    popup.innerHTML = slideList[active].popup;

    console.log(slideList[active]);
    console.log(popup);

    // indexInterval = setInterval(changeSlide, time);
  }
};

const buttonLeft = () => {
  if (active == 0) {
    active = slideList.length;
  }
  active--;
  image.src = slideList[active].img;
  h1.textContent = slideList[active].text;
  popup.innerHTML = slideList[active].popup;

  console.log(active);
};

const buttonRight = () => {
  if (active === slideList.length - 1) {
    active = 0;
  } else {
    active++;
  }
  image.src = slideList[active].img;
  h1.textContent = slideList[active].text;
  popup.innerHTML = slideList[active].popup;
  console.log(active);
};

window.addEventListener("keydown", keyChangeSlide);
btnRight.addEventListener("click", buttonRight);
btnLeft.addEventListener("click", buttonLeft);
